// JavaScript is a dynamic, weakly typed programming language.
// typeof: 데이터 타입을 확인하고 값을 타입 문자열로 반환
let variable;
console.log(typeof variable);

variable = '';
console.log(typeof variable);

variable = 123;
console.log(typeof variable);

variable = {};
console.log(typeof variable);

variable = function () {};
console.log(typeof variable);

console.log(typeof 123);
console.log(typeof '123');